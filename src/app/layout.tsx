import type { Metadata } from "next";
import { cookies } from "next/headers";
import { Inter } from "next/font/google";
import { createClient } from "@/utils/supabase/server";
import Header from "../components/Header";
import Footer from "../components/Footer";
import "../styles/globals.css";
import ToastMessagesProvider from "@/components/ToastMessages";

export const metadata: Metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

const inter = Inter({ subsets: ["latin"] });

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const supabase = createClient();
  const {
    data: { user },
    error,
  } = await supabase.auth.getUser();

  const {
    data: { user: supabaseAuthUser },
    error: supabaseAuthUserError,
  } = await supabase.auth.getUser();

  const { data: blogUser } = await supabase
    .from("blog_author")
    .select("*")
    .eq("auth_users_id", supabaseAuthUser?.id)
    .in("privileges", [2, 3])
    .single();

  const theme = cookies().get("theme")?.value || "";

  return (
    <html lang="pt">
      <body
        id="body"
        className={`${inter.className} ${theme} relative min-h-svh scrollbar dark:dark-scrollbar overflow-x-hidden flex flex-col justify-center items-center bg-base-100 dark:bg-dark-base-100 halftone dark:halftone-dark`}
      >
        <Header user={user} privileges={blogUser?.privileges} theme={theme} />
        <ToastMessagesProvider>
          <main className="w-full h-full flex-[2_2_0]">{children}</main>
        </ToastMessagesProvider>
        <Footer />
      </body>
    </html>
  );
}
